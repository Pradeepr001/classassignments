using System;

class task8
{
    static void Main()
    {
        // Display numbers from 100 to 5 with a gap of 3 using a for loop
        Console.WriteLine("Numbers from 100 to 5 with a gap of 3 using for loop:");
        for (int i = 100; i >= 5; i -= 3)
        {
            Console.WriteLine(i);
        }
    }
}
